<div class="chapter chapterthree"><div class="margin">

	<h3>Ruth &amp; Arthur Smadbeck Revitalize the&nbsp;Foundation and Its Programs <span>1930&rsquo;s</span></h3>

	<p>August Heckscher turned to Arthur Smadbeck and Ruth Smadbeck, financial equals who shared his deep dedication to public service. Arthur Smadbeck was one of the first and most prolific suburban real estate developers of the era. Until his death in 1977, Arthur Smadbeck donated his time and efforts to building an endowment and creating a profitable platform on which he positioned the Foundation to support major outside charitable efforts, while at the same time running his own successful businesses and extensive philanthropic endeavors.</p>

	<div class="imageblock">
		<img src="http://ihartley.wpengine.com/wp-content/uploads/2013/11/chapter3.jpg" alt="">
		<p>Ruth Smadbeck believed that institutions like the New York Zoological Society helped provide youth with important learning opportunities</p>
	</div>

	<p>Ruth Smadbeck began as a volunteer several years after the Foundation&rsquo;s building opened and ran the Foundation for over 50 years, including its multifaceted programs of dance, orchestra, exercise, swimming, the purchase and distribution of necessities for indigent children, a kindergarten, a theater, a craft room, a senior lounge, a photography group, a library, and a thrift shop, while at the same time broadcasting two radio programs a week offering advice and guidance on child care.</p>

</div></div>

<div class="chapter chapterfour"><div class="margin">

	<h3>The Modern Day Foundation Evolves <span>1980&rsquo;s</span></h3>

	<p>At Ruth Smadbeck&rsquo;s death in 1986 distributions to charity had grown to $1,169,219 and assets had grown to $22,072,773. Louis Smadbeck, a renowned real estate entrepreneur and civic leader in his own right, became Chairman of the Heckscher Foundation in 1986 and continued in this capacity until his death in 1992. Virginia Sloane was elected President in 1986 and President Emeritus in 2012.</p>

