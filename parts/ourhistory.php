	<p>Charles August Heckscher, a visionary who achieved great financial success, believed that wealth should be shared with others less fortunate, and thus as one of his many benefactions started the Foundation. Arthur Smadbeck, a friend of August Heckscher and fellow philanthropist, reluctantly took over a shattered financial and management structure and made possible the survival and emergence of The Heckscher Foundation for Children as a major benefactor. Ruth Smadbeck, who ran the Foundation for more than 50 years brought to its philanthropic activities a lifelong dedication to and love for children.</p>

</div></div>

<div class="chapter chaptertwo"><div class="margin">

	<h3>Foundation Building Opens <span>1922</span></h3>

	<p>The Heckscher Foundation was founded in 1921. The assets consisted of land at Fifth Avenue between 104th and 105th Streets in New York City and securities intended to provide funds for the construction of a building on the site and for its operations. The original Foundation building opened in 1922 but by 1922 was far exceeding its operating costs. As the Depression deepened, the Foundation assets were in default, and the Foundation itself was on the verge of collapse.</p>

