<div class="chapter chapterfive"><div class="margin">

	<h3>A New Generation Assumes Leadership Roles <span>1990&rsquo;s</span></h3>

	<p>A new generation assumed leadership roles in 1997. Howard G. (Peter) Sloane became Chairman and CEO and continues to preside over the Foundation&rsquo;s many philanthropic projects. Today, the Foundation&rsquo;s assets have grown to well over $300 million and distributions to charity have dramatically increased.</p>

